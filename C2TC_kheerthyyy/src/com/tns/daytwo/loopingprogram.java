package com.tns.daytwo;

public class loopingprogram {
	public static void main(String[]args) {
		// For loop
        System.out.println("For loop:");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        // While loop
        System.out.println("\nWhile loop:");
        int j = 1;
        while (j <= 5) {
            System.out.println(j);
            j++;
        }

        



}

}


